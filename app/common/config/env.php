<?php
/**
 * Объявления глобальных констант
 */

/**
 * @var  boolean Указывает работает ли приложение в отладочном режиме. По-умолчанию значение YII_DEBUG равно false
 */
defined('YII_DEBUG') or define('YII_DEBUG', true);

/**
 * @var  string Указывает в каком окружении запущено приложение. По-умолчанию значение YII_ENV равно 'prod'
 */
defined('YII_ENV') or define('YII_ENV', 'dev');

/**
 * @var  boolean Указывает нужно ли включать имеющийся в Yii обработчик ошибок. По-умолчанию значение равно true
 */
defined('YII_ENABLE_ERROR_HANDLER') or define('YII_ENABLE_ERROR_HANDLER', true);


